class Solution {
public:
    int maxProfit(int k, vector<int>& prices) {
        int n = prices.size();
        if (n == 0 || k == 0) return 0;

        // If k is large enough, it reduces to the unlimited transactions case
        if (k >= n / 2) {
            int maxProfit = 0;
            for (int i = 1; i < n; i++) {
                if (prices[i] > prices[i - 1]) {
                    maxProfit += prices[i] - prices[i - 1];
                }
            }
            return maxProfit;
        }

        // DP solution: dp[i][j] represents the max profit at the end of day j with at most i transactions
        vector<vector<int>> dp(k + 1, vector<int>(n, 0));

        for (int i = 1; i <= k; i++) {
            int maxDiff = -prices[0];
            for (int j = 1; j < n; j++) {
                dp[i][j] = max(dp[i][j - 1], prices[j] + maxDiff); // No transaction or sell on day j
                maxDiff = max(maxDiff, dp[i - 1][j] - prices[j]);  // Update maxDiff for the next day
            }
        }

        return dp[k][n - 1];
    }
};